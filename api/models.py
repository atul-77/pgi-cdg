class UserManager(BaseUserManager):
    use_in_migrations = True
    def create_user(self, username, name, category, password=None):
        user = self.model(
            username=username,
            category=category,
            name=name,
        )
        user.set_password(password)
        user.save(using=self._db)
        return user
    def create_staffuser(self, email, name, date_of_birth, password):
        user = self.create_user(
            email,
            password=password,
            date_of_birth=date_of_birth,         
            name=name,
        )
        user.staff = True
        user.save(using=self._db)
        return user
    
    def create_superuser(self, username, name, category, password):
        user = self.create_user(
            username=username,
            category=category,
            name="True",
        )
        user.staff = True
        user.admin = True
        user.save(using=self._db)
        return user

